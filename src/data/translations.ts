export function t(locale: Locale, key: string, params?: Record<string, string>): string {
  const value = site[locale]?.[key] ?? site.en[key] ?? key;
  if (params) {
    return Object.entries(params).reduce((str, [k, v]) => str.replace(`{${k}}`, v), value);
  }
  return value;
}

